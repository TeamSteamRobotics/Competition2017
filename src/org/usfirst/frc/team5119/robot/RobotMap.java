package org.usfirst.frc.team5119.robot;

/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {
	 public final static int frontLeftMotor = 3; //Back Right = 2
	 public final static int frontRightMotor = 0; //Correct = 0
	 public final static int backLeftMotor = 1;//Front Left = 3
	 public final static int backRightMotor = 2; //Back Left = 1
	
}
